class Restaurant {
  final int restaurantId;
  final String name;
  final String location;
  final String price;
  final String image;
  final String number;
  final double initialRating;

  Restaurant({
    required this.restaurantId,
    required this.name,
    required this.location,
    required this.price,
    required this.image,
    required this.number,
    required this.initialRating,
  });
}
